    private void EstatusCorteCaja(){
    
     float cantidad1 = Float.parseFloat(lblTotalV.getText());
    float cantidad2 = Float.parseFloat(lblTotalC.getText());
    
    if (cantidad1 == cantidad2) {
        JOptionPane.showMessageDialog(null, "El corte de caja ha sido exitoso");
    } else if (cantidad1 > cantidad2) {
        float falta = cantidad1 - cantidad2;
        JOptionPane.showMessageDialog(null, "Hace falta $" + falta + " de dinero");
    } else { // cantidad1 < cantidad2
        float sobra = cantidad2 - cantidad1;
        JOptionPane.showMessageDialog(null, "Sobra $" + sobra + " de dinero");
    }
   
    LimpiarCampos();
    }

