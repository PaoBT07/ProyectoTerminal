    //Método para limitar a escribir solo números en los Textfield
//------------------------------------------------------------------------------
    private void limitarNumeros(JTextField textField) {
    textField.addKeyListener(new KeyAdapter() {
        public void keyTyped(KeyEvent e) {
            char c = e.getKeyChar();
            
            // Si no es un dígito ni un punto
            if (!Character.isDigit(c) && c != '.') {
                e.consume(); // Bloquea
            }

            // Si ya hay un punto y se quiere escribir otro
            if (c == '.' && textField.getText().contains(".")) {
                e.consume(); // Bloquea el segundo punto
            }
        }
    });
}
