    public void Mostrar(String tabla) {
    
    String sql = "SELECT lp.idListaPedido, lp.LP_Fecha, cr.CR_Nombre, lp.LP_ImporteTotal " +
                 "FROM ListaPedido lp " +
                 "INNER JOIN ClienteReparto cr ON lp.LP_IDClienteReparto = cr.idClienteReparto " +
                 "WHERE DATE(lp.LP_Fecha) = CURDATE()";

    Conexion con = new Conexion();
    java.sql.Connection conexion = con.conectar();

    DefaultTableModel model = new DefaultTableModel();
    String[] columnas = {"ID", "Fecha", "Nombre Cliente Reparto", "Total a pagar"};
    
    for (String columna : columnas) {
        model.addColumn(columna);
    }

    TablaReportesVenta.setModel(model);

    try (java.sql.Statement st = conexion.createStatement(); ResultSet rs = st.executeQuery(sql)) {
        while (rs.next()) {
            String[] datos = new String[columnas.length];
            datos[0] = rs.getString("idListaPedido");
            datos[1] = rs.getString("LP_Fecha");
            datos[2] = rs.getString("CR_Nombre");
            datos[3] = rs.getString("LP_ImporteTotal");
            model.addRow(datos);
        }

        // Ajustar ancho de columnas
        TableColumnModel modeloColumnas = TablaReportesVenta.getColumnModel();
        modeloColumnas.getColumn(0).setPreferredWidth(1);   // ID
        modeloColumnas.getColumn(1).setPreferredWidth(30);  // Fecha
        modeloColumnas.getColumn(2).setPreferredWidth(100); // Nombre Cliente
        modeloColumnas.getColumn(3).setPreferredWidth(30);  // Total a Pagar

    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error en executeQuery: " + e.getMessage());
    } finally {
        try {
            if (conexion != null && !conexion.isClosed()) {
                conexion.close();
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error al cerrar la conexión: " + ex.getMessage());
        }
    }
}
