    //Método para agregar los datos de los TxtFields a la tabla ReportesVenta
//------------------------------------------------------------------------------
    private void Agregar() {                                            
                                           
    Date fechaRV = parseDate(lblFecha.getText()); 
    float totalRepartos = Float.parseFloat(txtTotalRepartos.getText());
    float ventasRegistradas = Float.parseFloat(txtTotalVentasRegistradas.getText());
    float totalVenta = Float.parseFloat(lblTotalV.getText());
    float gastos = Float.parseFloat(txtGastos.getText());
    float monedas = Float.parseFloat(txtPrestamoMonedas.getText());
    int billetes = Integer.parseInt(txtPrestamoBilletes.getText());
    float dineroCaja = Float.parseFloat(txtDineroCaja.getText());
    float totalCaja = Float.parseFloat(lblTotalC.getText());
     
    String seleccionado = cmbUsuario.getSelectedItem().toString();
    String idString = seleccionado.split(" - ")[0]; // Tomamos solo el ID antes del guión
    int idUsuario = Integer.parseInt(idString);
    
    if (fechaRV == null) {
        JOptionPane.showMessageDialog(null, "Fecha inválida. Ingrese en formato yyyy-MM-dd.");
        return; // Sale del método si la fecha es inválida
    }
    
    java.sql.Date fechaSQL = new java.sql.Date(fechaRV.getTime()); 

    try {
        java.sql.Connection con =  Conexion.conectar(); 
        PreparedStatement ps = con.prepareStatement("INSERT INTO reporteventas (RV_Fecha, RV_Gastos, RV_PrestamoMonedas, RV_PrestamoBilletes, RV_DineroCaja, RV_TotalCaja, RV_VentasRegistradas, RV_VentasRepartos, RV_TotalVenta, RV_IDUsuario) VALUES (?,?,?,?,?,?,?,?,?,?)");

        ps.setDate(1, fechaSQL);
        ps.setFloat(2, gastos); 
        ps.setFloat(3, monedas); 
        ps.setInt(4, billetes); 
        ps.setFloat(5, dineroCaja);
        ps.setFloat(6, totalCaja);
        ps.setFloat(7, ventasRegistradas);
        ps.setFloat(8, totalRepartos);
        ps.setFloat(9, totalVenta);
        ps.setInt(10, idUsuario);
        

        ps.executeUpdate();
        JOptionPane.showMessageDialog(null, "Registro guardado");
        
        //Actualizar la tabla para que aparezca el registro nuevo
        Mostrar("reporteventas");
        
        
        con.close(); // Cierra la conexión

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error al guardar: " + e.getMessage());
    }
    }
