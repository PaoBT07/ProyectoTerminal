    public void SumaImporteTotal() {
    String sql = "SELECT SUM(LP_ImporteTotal) AS Total " +
                 "FROM ListaPedido " +
                 "WHERE DATE(LP_Fecha) = CURDATE()";

    Conexion con = new Conexion();
    java.sql.Connection conexion = con.conectar();

    try (PreparedStatement pst = conexion.prepareStatement(sql)) {
        ResultSet rs = pst.executeQuery();

        if (rs.next()) {
            double total = rs.getDouble("Total");
            txtTotalRepartos.setText(String.valueOf(total));
        } else {
            txtTotalRepartos.setText("0.0");
        }

    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error al calcular el total del día: " + e.getMessage());
    } finally {
        try {
            if (conexion != null && !conexion.isClosed()) {
                conexion.close();
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error al cerrar la conexión: " + ex.getMessage());
        }
    }
}
