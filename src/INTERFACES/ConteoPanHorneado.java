private void Guardar(){

    Date fecha = parseDate(lblFecha.getText());     
    int totalPB = Integer.parseInt(txtTotalPB.getText()); 
    int totalPD = Integer.parseInt(txtTotalPD.getText());  
    
    if (fecha == null) {
        JOptionPane.showMessageDialog(null, "Fecha invÃ¡lida. Ingrese en formato yyyy-MM-dd.");
        return; // Sale del mÃ©todo si la fecha es invÃ¡lida
    }
    
    java.sql.Date fechaSQL = new java.sql.Date(fecha.getTime()); 
    
    try {
        java.sql.Connection con =  Conexion.conectar(); 
        PreparedStatement ps = con.prepareStatement("INSERT INTO conteo_panhorneado (CPH_Fecha, CPH_TotalPiezasPB, CPH_TotalPiezasPD) VALUES (?,?,?)");

        ps.setDate(1, fechaSQL);
        ps.setInt(2, totalPB);
        ps.setInt(3, totalPD); 

        ps.executeUpdate();
        JOptionPane.showMessageDialog(null, "Registro guardado");
        
        //Actualizar la tabla para que aparezca el registro nuevo
        Mostrar("conteo_panhorneado");
        
        //DespuÃ©s de guardar el registro limpiar los campos 
        LimpiarCampos();
        
        con.close(); // Cierra la conexiÃ³n

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error al guardar: " + e.getMessage());
    }

}
