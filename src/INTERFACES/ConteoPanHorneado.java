    //MÃ©todo para mostrar los registros de la entidad EQUIPO
//------------------------------------------------------------------------------
    public void Mostrar(String tabla) {
    String sql = "SELECT * FROM " + tabla + " WHERE CPH_Fecha >= NOW() - INTERVAL 3 WEEK";
    Conexion con = new Conexion();
    java.sql.Connection conexion = con.conectar();

    DefaultTableModel model = new DefaultTableModel();
    String[] columnas = {"ID", "Fecha", "Total Piezas Pan Blanco", "Total Piezas Pan Dulce"};

    for (String columna : columnas) {
        model.addColumn(columna);
    }

    TablaConteoPan.setModel(model);

    try (java.sql.Statement st = conexion.createStatement(); ResultSet rs = st.executeQuery(sql)) {
        while (rs.next()) {
            String[] datos = new String[columnas.length];
            for (int i = 0; i < columnas.length; i++) {
                datos[i] = rs.getString(i + 1);
            }

            model.addRow(datos);
        }

        // Ajustar ancho de columnas
        TableColumnModel modeloColumnas = TablaConteoPan.getColumnModel();
        modeloColumnas.getColumn(0).setPreferredWidth(1);  // ID
        modeloColumnas.getColumn(1).setPreferredWidth(10); // Fecha
        modeloColumnas.getColumn(2).setPreferredWidth(10); // Piezas PB
        modeloColumnas.getColumn(3).setPreferredWidth(10); // Piezas PD

    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error en executeQuery: " + e.getMessage());
    } finally {
        try {
            if (conexion != null && !conexion.isClosed()) {
                conexion.close();
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error al cerrar la conexiÃ³n: " + ex.getMessage());
        }
    }
}
