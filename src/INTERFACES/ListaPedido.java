	// Método para mandar a imprimir al presionar al boton IMPRIMIR
//----------------------------------------------------------------

private void Imprimir(){
        PrinterJob job = PrinterJob.getPrinterJob();
        
        job.setPrintable(pedido1);
        if(job.printDialog()){
        try{
            job.print();
        }
        catch(PrinterException ex){
        }
        } else {
        JOptionPane.showMessageDialog(this, "La impresion fue cancelada");
        }
}
