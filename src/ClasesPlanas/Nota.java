    // Obtener el último número de remisión generado
    private String obtenerUltimoNumero() throws SQLException {
        String sql = "SELECT LP_NumNotaRemision FROM listapedido ORDER BY idListaPedido DESC LIMIT 1";
        try (PreparedStatement ps = con.prepareStatement(sql);
             ResultSet rs = ps.executeQuery()) {
            if (rs.next()) {
                return rs.getString("LP_NumNotaRemision");
            } else {
                return "000000"; // Si no hay registros todavía
            }
        }
    }

