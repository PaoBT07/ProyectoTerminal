    //Método para agregar los datos de los TxtFields a la tabla ReportesVenta
//------------------------------------------------------------------------------
    public void Agregar() {
    Date fecha = parseDate(lblFecha.getText()); 
    if (fecha == null) {
        JOptionPane.showMessageDialog(null, "Fecha inválida. Ingrese en formato yyyy-MM-dd.");
        return;
    }

    String seleccionadoCliente = cmbCliente.getSelectedItem().toString();
    if (seleccionadoCliente.equals("Seleccione cliente")) {
        JOptionPane.showMessageDialog(null, " No se guardará el pedido.");
        return; // Aquí detenemos el guardado pero sin marcar error
    }

    java.sql.Date fechaSQL = new java.sql.Date(fecha.getTime());

    int totalPB = Integer.parseInt(txtTeleras.getText().trim()) +
                  Integer.parseInt(txtBolilloLiso.getText().trim()) +
                  Integer.parseInt(txtBolilloAjonjoli.getText().trim());

    int totalPD = Integer.parseInt(txtBizcocho.getText().trim()) +
                  Integer.parseInt(txtFeite.getText().trim());

    float importeTotal = Float.parseFloat(lblTotal.getText().trim());

    String seleccionadoCajera = cmbCajera.getSelectedItem().toString();
    int idUsuarioCajera = Integer.parseInt(seleccionadoCajera.split(" - ")[0]);
    int idUsuarioCliente = Integer.parseInt(seleccionadoCliente.split(" - ")[0]);

    try {
        Connection con = Conexion.conectar();

        String numeroNota = generarSiguienteNumero(con);

        PreparedStatement ps = con.prepareStatement(
            "INSERT INTO listapedido (LP_Fecha, LP_NumNotaRemision, LP_TotalPanBlanco, LP_TotalPanDulce, LP_ImporteTotal, LP_IDClienteReparto, LP_IDUsuario) VALUES (?, ?, ?, ?, ?, ?, ?)"
        );

        ps.setDate(1, fechaSQL);
        ps.setString(2, numeroNota);
        ps.setInt(3, totalPB);
        ps.setInt(4, totalPD);
        ps.setFloat(5, importeTotal);
        ps.setInt(6, idUsuarioCliente);
        ps.setInt(7, idUsuarioCajera);

        ps.executeUpdate();
        JOptionPane.showMessageDialog(null, "Registro guardado correctamente.");
        con.close();

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error al guardar: " + e.getMessage());
    }

}
