    //Método para calcular los subtotales
//------------------------------------------------------------------------------
private void subtotales(){

    try {
        // Verificamos que todos los campos tengan contenido
        if (!txtTeleras.getText().isEmpty() &&
            !txtBolilloLiso.getText().isEmpty() &&
            !txtBolilloAjonjoli.getText().isEmpty() &&
            !txtBizcocho.getText().isEmpty() &&
            !txtFeite.getText().isEmpty() &&
            !txtCantidadCajas.getText().isEmpty()) {

            float subtotalTelera = Float.parseFloat(txtTeleras.getText()) * 2.00f;
            float subtotalBolillo = Float.parseFloat(txtBolilloLiso.getText()) * 2.00f;
            float subtotalAjonjoli = Float.parseFloat(txtBolilloAjonjoli.getText()) * 2.50f;
            float subtotalBizcocho = Float.parseFloat(txtBizcocho.getText()) * 10.00f;
            float subtotalFeite = Float.parseFloat(txtFeite.getText()) * 11.00f;
            float subtotalCajas = Float.parseFloat(txtCantidadCajas.getText()) * 10.00f;

            // Mostrar los resultados
            lblTelera.setText(String.format("%.2f", subtotalTelera));
            lblBolilloLiso.setText(String.format("%.2f", subtotalBolillo));
            lblBolilloAjonjoli.setText(String.format("%.2f", subtotalAjonjoli));
            lblBizcocho.setText(String.format("%.2f", subtotalBizcocho));
            lblFeite.setText(String.format("%.2f", subtotalFeite));
            lblCantidadCajas.setText(String.format("%.2f", subtotalCajas));
        } else {
            // Si algún campo está vacío, mostramos 0
            lblTelera.setText("0.00");
            lblBolilloLiso.setText("0.00");
            lblBolilloAjonjoli.setText("0.00");
            lblBizcocho.setText("0.00");
            lblFeite.setText("0.00");
            lblCantidadCajas.setText("0.00");
        }
    } catch (NumberFormatException e) {
        
        lblTelera.setText("0.00");
        lblBolilloLiso.setText("0.00");
        lblBolilloAjonjoli.setText("0.00");
        lblBizcocho.setText("0.00");
        lblFeite.setText("0.00");
        lblCantidadCajas.setText("0.00");
    }
}
