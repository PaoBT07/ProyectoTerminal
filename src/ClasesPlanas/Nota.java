   public void llenarComboBoxCliente(JComboBox<String> comboBox, Connection conn) {
    String sql = "SELECT idClienteReparto, CR_Nombre, CR_Telefono FROM clientereparto"; 
    Connection con = Conexion.conectar();

    try (PreparedStatement stmt = con.prepareStatement(sql);
         ResultSet rs = stmt.executeQuery()) {

        comboBox.removeAllItems(); // Limpiar el JComboBox antes de llenarlo
        comboBox.addItem("Seleccione cliente");
        
        while (rs.next()) {
            int idCliente = rs.getInt("idClienteReparto");
            String nombre = rs.getString("CR_Nombre");
            String telefono = rs.getString("CR_Telefono");
            
            String item = idCliente + " - " + nombre + ", " + telefono;  // Ej: "3 - Ana Torres, 5551234567"
            comboBox.addItem(item);
        }

    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error al cargar datos en el ComboBox.");
    }
}
