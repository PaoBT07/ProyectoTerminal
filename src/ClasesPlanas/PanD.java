    //Método para calcular el total de teleras
//------------------------------------------------------------------------------
private void totalBizcocho() {
    JTextField[] campos = {
        txtAlmeja, txtBeso, txtBigote, txtCacahuate, txtCema, txtChochito, txtCocol, txtColchon,
        txtConchaBlanca, txtConchaChocolate, txtConchaAmarilla, txtCuernito, txtEmpanada, txtEspejo,
        txtGendarme, txtHojaldra, txtHueso, txtLadrillo, txtMantecada, txtMaria, txtMazapan,
        txtMono, txtMordida, txtNovia, txtNuez, txtPiedra, txtPiojosa, txtPolvoron, txtRosca, txtBisquet
    };

    int totalB = 0;

    try {
        for (JTextField campo : campos) {
            String texto = campo.getText().trim();
            if (texto.isEmpty()) {
                campo.setText("0");
                texto = "0";
            }
            totalB += Integer.parseInt(texto);
        }

        lblTotalBiz.setText(String.valueOf(totalB));
    } catch (NumberFormatException e) {
        // Si algún campo no es número, se reinician todos a 0
        for (JTextField campo : campos) {
            campo.setText("0");
        }
        lblTotalFeite.setText("0");
    }
}
