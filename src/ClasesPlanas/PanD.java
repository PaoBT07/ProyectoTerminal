    //Método para cantidades de materias primas
//------------------------------------------------------------------------------
    
    public void MateriasPrimasPorProduccion(int totalPiezas) {
    
    int piezasPorBultoHarina = 400;
    int piezasPorBultoAzucar = 2000;
    int piezasPorKiloGrasaVegetal = 167;
    int piezasPorBoteAceite = 2000;
    int piezasPorCasilleroHuevo = 200;
    int piezasPorKilosMantequilla = 500;
    int piezasPorCajaLevadura = 400;

    // Calcular cantidades necesarios, redondeando hacia arriba
    int bultosHarina = (int) Math.ceil((double) totalPiezas / piezasPorBultoHarina);
    int bultosAzucar = (int) Math.ceil((double) totalPiezas / piezasPorBultoAzucar);
    int kiloGrasa = (int) Math.ceil((double) totalPiezas / piezasPorKiloGrasaVegetal);
    int boteAceite = (int) Math.ceil((double) totalPiezas / piezasPorBoteAceite);
    int casilleroHuevo = (int) Math.ceil((double) totalPiezas / piezasPorCasilleroHuevo);
    int kilosMantequilla = (int) Math.ceil((double) totalPiezas / piezasPorKilosMantequilla);
    int cajaLevadura = (int) Math.ceil((double) totalPiezas / piezasPorCajaLevadura);

   
    txtHarina.setText(String.valueOf(bultosHarina));
    txtAzucar.setText(String.valueOf(bultosAzucar));
    txtGrasaVegetal.setText(String.valueOf(kiloGrasa));
    txtAceite.setText(String.valueOf(boteAceite));
    txtHuevos.setText(String.valueOf(casilleroHuevo));
    txtMantequilla.setText(String.valueOf(kilosMantequilla));
    txtLevadura.setText(String.valueOf(cajaLevadura));
}

    
