    private int SumaPiezasPB() {
    int totalPiezasPB = 0;

    int totalTeleras = parseEntero(txtTeleras);
    int totalBL = parseEntero(txtBolilloLiso);
    int totalBA = parseEntero(txtBolilloAjonjoli);

    totalPiezasPB = totalTeleras + totalBL + totalBA;
    return totalPiezasPB;
}
//------------------------------------------------------------------------------
    
    private int parseEntero(JTextField campo) {
    String texto = campo.getText().trim();
    if (texto.isEmpty()) {
        campo.setText("0");
        return 0;
    }
    try {
        return Integer.parseInt(texto);
    } catch (NumberFormatException e) {
        campo.setText("0");
        return 0;
    }
}
    
