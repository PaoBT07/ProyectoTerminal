//------------------------------------------------------------------------------
 
public void llenarComboBox(JComboBox<String> comboBox, Connection conn) {
    String sql = "SELECT CR_Nombre, CR_Telefono FROM clientereparto"; 
    Connection con = Conexion.conectar();
     
    try (PreparedStatement stmt = con.prepareStatement(sql);
         ResultSet rs = stmt.executeQuery()) {

        comboBox.removeAllItems(); // Limpiar el JComboBox antes de llenarlo
        comboBox.addItem("Seleccione un cliente...");

        while (rs.next()) {
            String nombre = rs.getString("CR_Nombre");
            String telefono = rs.getString("CR_Telefono");
            String item = nombre + ", " + telefono; // Concatenar con coma
            comboBox.addItem(item);
        }

    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error al cargar datos en el ComboBox.");
    }
}
